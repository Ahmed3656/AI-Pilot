import assert from 'node:assert/strict';
import { readFileSync } from 'node:fs';

const read = (path) =>
  readFileSync(new URL(`../${path}`, import.meta.url), 'utf8');
const root = JSON.parse(read('package.json'));
const lock = JSON.parse(read('package-lock.json'));

assert.equal(lock.lockfileVersion, 3);
assert.deepEqual(lock.packages[''].workspaces, root.workspaces);
assert.deepEqual(lock.packages[''].devDependencies, root.devDependencies);
for (const workspace of root.workspaces) {
  const manifest = JSON.parse(read(`${workspace}/package.json`));
  const locked = lock.packages[workspace];
  assert.ok(locked, `${workspace} is represented in package-lock.json`);
  assert.deepEqual(locked.dependencies ?? {}, manifest.dependencies ?? {});
  assert.deepEqual(
    locked.devDependencies ?? {},
    manifest.devDependencies ?? {},
  );
}

for (const name of ['requirements.lock.txt', 'requirements-dev.lock.txt']) {
  const contents = read(`services/ai-service/${name}`);
  assert.match(contents, /autogenerated by pip-compile with Python 3\.12/);
  assert.match(contents, /--hash=sha256:/);
  for (const dependency of [
    'fastapi',
    'httpx',
    'openai',
    'pydantic-settings',
    'selenium',
    'uvicorn',
  ]) {
    assert.match(contents, new RegExp(`^${dependency}==`, 'm'));
  }
}

const developmentLock = read('services/ai-service/requirements-dev.lock.txt');
for (const dependency of ['pytest', 'pytest-asyncio', 'ruff']) {
  assert.match(developmentLock, new RegExp(`^${dependency}==`, 'm'));
}

console.log('JavaScript and Python dependency lock validation passed.');
